package ders02;

public class Variables {
    public static void main(String[] args) {
        int sayi; // sadece 1 kere yapılır.(int)

        //istersek sonradan alttaki gibi yazarak bir sayı verebiliyoruz.
        sayi=15;
        System.out.println(sayi);

        sayi=20;
        System.out.println(sayi);

        sayi = sayi + 10; // eşitliğin sağ tarafından başladı 30 buldu soldaki sayi değerine 30 dedi.
        System.out.println(sayi);


    }
}

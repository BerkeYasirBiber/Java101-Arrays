package ders16;

public class C01_whileLoop {
    public static void main(String[] args) {

        for (int i = 10; i <100 ; i++) {
            System.out.print(i +" ");
        }

        System.out.println("");

        int a=10;
        while (a<100){
            System.out.print(a +" ");
            a++;
        }


    }
}

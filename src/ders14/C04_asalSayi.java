package ders14;

public class C04_asalSayi {
    public static void main(String[] args) {

        int sayi=15;


        for (int i = 2; i <=sayi-1 ; i++) {

            if (sayi%i==0){

                System.out.println("asal sayi degil");

                break;

            } else System.out.println("asal sayi");

                break;

        }












    }
}
